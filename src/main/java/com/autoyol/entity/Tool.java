package com.autoyol.entity;

public class Tool {
	private String tool_id;
	private String toolbox_id;
	private String status;
	private String tool_type;
	private String tool_name;
	private String tool_desc;
	private String tool_createtime;
	private String tool_lastmodifytime;
	public String getTool_id() {
		return tool_id;
	}
	public void setTool_id(String tool_id) {
		this.tool_id = tool_id;
	}
	public String getToolbox_id() {
		return toolbox_id;
	}
	public void setToolbox_id(String toolbox_id) {
		this.toolbox_id = toolbox_id;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getTool_type() {
		return tool_type;
	}
	public void setTool_type(String tool_type) {
		this.tool_type = tool_type;
	}
	public String getTool_name() {
		return tool_name;
	}
	public void setTool_name(String tool_name) {
		this.tool_name = tool_name;
	}
	public String getTool_desc() {
		return tool_desc;
	}
	public void setTool_desc(String tool_desc) {
		this.tool_desc = tool_desc;
	}
	public String getTool_createtime() {
		return tool_createtime;
	}
	public void setTool_createtime(String tool_createtime) {
		this.tool_createtime = tool_createtime;
	}
	public String getTool_lastmodifytime() {
		return tool_lastmodifytime;
	}
	public void setTool_lastmodifytime(String tool_lastmodifytime) {
		this.tool_lastmodifytime = tool_lastmodifytime;
	}
	
	
}
